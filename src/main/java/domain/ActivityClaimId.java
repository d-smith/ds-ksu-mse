package domain;

/**
 * Class representing the data associated with an activity claim id.
 *
 */
public final class ActivityClaimId implements java.io.Serializable {


	private static final long serialVersionUID = 2370746715462025922L;
	private String userid;
	private long activityinstanceid;

	public ActivityClaimId() {
	}

	public ActivityClaimId(String userid, long activityinstanceid) {
		this.userid = userid;
		this.activityinstanceid = activityinstanceid;
	}

	public String getUserid() {
		return this.userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public long getActivityinstanceid() {
		return this.activityinstanceid;
	}

	public void setActivityinstanceid(long activityinstanceid) {
		this.activityinstanceid = activityinstanceid;
	}



}
