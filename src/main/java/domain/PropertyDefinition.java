package domain;


/**
 * Representation of a property definition.
 *
 */
public final class PropertyDefinition implements java.io.Serializable {


	private static final long serialVersionUID = -6918469149837360414L;
	private long propertydefinitionid;
	private String name;
	private String description;
	private long type;

	public PropertyDefinition() {
	}

	public long getPropertydefinitionid() {
		return this.propertydefinitionid;
	}

	public void setPropertydefinitionid(long propertydefinitionid) {
		this.propertydefinitionid = propertydefinitionid;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return this.description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public long getType() {
		return this.type;
	}

	public void setType(long type) {
		this.type = type;
	}

}
